# Helper to find lead_id by phone number
find_lead = fn callee ->
  case Repo.query("SELECT id FROM leads WHERE telefon = $1 OR telefon LIKE $2 LIMIT 1", [callee, "%" <> callee]) do
    {:ok, %{rows: [[id]]}} -> Saleflow.Sales.decode_uuid(id)
    _ -> nil
  end
end

# Helper to insert a phone call
insert_call = fn caller, callee, datetime_str, duration, direction, user_id ->
  {:ok, naive} = NaiveDateTime.from_iso8601(datetime_str <> ":00")
  {:ok, dt} = DateTime.from_naive(naive, "Etc/UTC")
  lead_id = find_lead.(callee)

  dir = case direction do
    "Utgående" -> "outgoing"
    "Missade" -> "missed"
    _ -> "outgoing"
  end

  Repo.query(
    "INSERT INTO phone_calls (id, caller, callee, duration, user_id, lead_id, direction, received_at, inserted_at) VALUES (gen_random_uuid(), $1, $2, $3, $4, $5, $6, $7, $7) ON CONFLICT DO NOTHING",
    [caller, callee, duration, user_id && Ecto.UUID.dump!(user_id), lead_id && Ecto.UUID.dump!(lead_id), dir, dt]
  )
end

